function mousePressed() {
  if (mouseX > 15 && mouseX < 45 && mouseY > 20 && mouseY < 75) {
    switch_state = !switch_state;
  }
}

function draw_switch() {
  outline_color = "#222222";
  fill(outline_color);
  circle(30, 30, 30);
  circle(30, 60, 30);
  rect(15, 30, 30, 30);
  switch_color = "#ffffff";
  noFill();
  stroke(switch_color);
  strokeWeight(3);
  if (switch_state == false) {
    circle(30, 30, 20);
  } else {
    circle(30, 60, 20);
  }
}
