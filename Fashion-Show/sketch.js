function setup() {
  createCanvas(400, 400);
    strokeCap(PROJECT);
  strokeJoin(MITER);

  strokeWeight(16);
}

function draw() {
  background(0);
  const num_lines = 40
  const line_spacing = int(width / num_lines)
  noStroke()

  push()
  translate(width/2,height/2)
  rotate(frameCount*0.04)
        fill(get_random_color())
  for (let i =0; i < num_lines;i++){

    rect(i*line_spacing,-height,3,height * 2)
    rect(-i*line_spacing,-height,3,height * 2)
  }
  pop()
  scale(0.2, 0.2);
  translate(200,200)
  draw_person();
  layer1(get_random_color())
  layer2(get_random_color())
  layer3(get_random_color())
  layer4(get_random_color())
  layer5(get_random_color())
}

function get_random_color(){
  return color(random(0,255),random(0,255),random(0,255))
}

